package com.example.myfood_lqhuy;

public class Food {
    public int id;
    public String name;
    public double price;
    public String image;
    public String description;

    public Food(int id, String name, double price, String image, String description) {
        this.id = id;
        this.name = name;
        this.price = price;
        this.image = image;
        this.description = description;
    }
}
